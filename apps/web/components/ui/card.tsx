import { cn } from "@/lib/utils";

const Card = React.forwardRef<HTMLDivElement, React.HTMLAttributes<HTMLDivElement>>(
  ({ className, ...props }, ref) => (
    <div
      ref={ref}
      className={cn("rounded-lg border border-white/10 bg-panel/80 text-slate-100 shadow-glow", className)}
      {...props}
    />
  ),
);
Card.displayName = "Card";

